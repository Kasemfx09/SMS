function drawChart() {
  var data = google.visualization.arrayToDataTable([
    ['Task', 'Hours per Day'],
    ['Fees',     10],
    ['Income',      15],
    ['Payable Salary',   11],
    ['Expenses',  4],
    
  
  ]);

  var options = {
    title: 'Income and Expense',
    is3D: true,
  };

  var chart = new google.visualization.PieChart(document.getElementById('piechart_3d'));
  chart.draw(data, options);
}

